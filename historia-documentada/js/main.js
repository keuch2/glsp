// Form: address visibility + submit
const shippingRadios=document.querySelectorAll('input[name=shipping]');
const addressGroup=document.getElementById('addressGroup');
if(addressGroup){
  shippingRadios.forEach(r=>r.addEventListener('change',()=>{
    const sel=document.querySelector('input[name=shipping]:checked');
    addressGroup.classList.toggle('visible',sel && sel.value!=='pickup');
  }));
}

const orderForm=document.getElementById('orderForm');
if(orderForm){
  orderForm.addEventListener('submit',async e=>{
    e.preventDefault();
    const f=e.target;
    if(!f.name.value || !f.email.value || !f.country.value){
      f.reportValidity();
      return;
    }
    const btn=f.querySelector('.form-submit');
    btn.disabled=true;
    const body=new FormData();
    body.append('action','pedido');
    body.append('nombre',f.name.value.trim());
    body.append('pais',f.country.value);
    body.append('ciudad',f.city?.value?.trim()||'');
    body.append('email',f.email.value.trim());
    body.append('telefono',f.phone?.value?.trim()||'');
    body.append('cantidad',f.qty?.value||1);
    body.append('envio',f.shipping.value);
    body.append('direccion',f.address?.value?.trim()||'');
    body.append('mensaje',f.message?.value?.trim()||'');
    try{
      const r=await fetch('submit.php',{method:'POST',body});
      const d=await r.json();
      if(d.ok){
        const confirm=document.getElementById('formConfirm');
        f.reset();
        if(addressGroup) addressGroup.classList.remove('visible');
        if(confirm){ confirm.classList.add('show'); setTimeout(()=>confirm.classList.remove('show'),6000); }
      } else {
        alert(d.error||'Error al enviar. Intente nuevamente.');
      }
    } catch{
      alert('Error de conexión. Intente nuevamente.');
    } finally{
      btn.disabled=false;
    }
  });
}

