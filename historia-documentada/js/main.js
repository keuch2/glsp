function setLanguage(lang){
  if(!translations[lang]) return;
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    const k=el.getAttribute('data-i18n');
    if(translations[lang][k]!==undefined){
      if(el.tagName==='TITLE') document.title=translations[lang][k];
      else el.textContent=translations[lang][k];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    const k=el.getAttribute('data-i18n-placeholder');
    if(translations[lang][k]!==undefined) el.placeholder=translations[lang][k];
  });
  document.querySelectorAll('.lang-btn').forEach(b=>b.classList.toggle('active',b.dataset.lang===lang));
  document.documentElement.lang=lang;
  localStorage.setItem('glsp_libro_lang',lang);
}

